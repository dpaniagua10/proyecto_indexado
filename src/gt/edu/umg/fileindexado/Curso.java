package gt.edu.umg.fileindexado;

public class Curso {

	private String nombre;
	private float nota;
	
	public Curso() {
		
	}
	
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public float getNota() {
		return nota;
	}
	public void setNota(float nota) {
		this.nota = nota;
	}
		
}
